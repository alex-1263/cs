#include<stdio.h>
int main()
{
	long t=0,s1=0,s2=0;
	scanf("%ld",&t);
	while(t>0)
	{
		if(t>= 10)
		{
			s1+=90;
			s2+=30;
			t-=10;
		}
		else  
		{
			s1+=9*t;
			s2+=3*t;
			t=0;
		}
		if(t>0&&s1>s2)
		{
			if(t>=30)
			{
				s2+=90;
				t-=30;
			}
			else
			{
				s2+=3*t;
				t=0;
			}
		}
		
	}
	if(s1>s2)
	{
		printf("^_^ %ld",s1);
	}
	if(s1<s2)
	{
		printf("@_@ %ld",s2);
	}
	if(s1==s2)
	{
		printf("-_- %d",s1);
	}
}
